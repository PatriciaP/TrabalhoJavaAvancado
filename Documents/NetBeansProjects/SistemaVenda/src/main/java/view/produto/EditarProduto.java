    private void preencherDados() {
        edtCod.setText(produto.getIdProduto());
        edtEstoqueMin.setText(String.valueOf(produto.getEstoqueMin()));
        edtNome.setText(produto.getNome());
        edtQtdEstoque.setText(String.valueOf(produto.getQtdEstoque()));
        edtValor.setText(Double.toString(produto.getValor()));
    }

    private boolean verificaCampos() {

        return (!edtNome.getText().isEmpty()
                && !edtCod.getText().isEmpty()
                && !edtEstoqueMin.getText().isEmpty()
                && !edtNome.getText().isEmpty()
                && !edtQtdEstoque.getText().isEmpty()
                && !edtValor.getText().isEmpty());
    }
