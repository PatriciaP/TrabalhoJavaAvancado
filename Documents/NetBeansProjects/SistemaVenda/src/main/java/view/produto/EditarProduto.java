    private void edtCodKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtCodKeyReleased
        // TODO add your handling code here:
        if (ProdutoService.buscarPorCodigo((edtCod.getText())) == null) {
            codExistente.setVisible(false);
            btnSalvar.setEnabled(true);
        } else {
            codExistente.setVisible(true);
            btnSalvar.setEnabled(false);
        }
    }//GEN-LAST:event_edtCodKeyReleased

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        if (verificaCampos()) {
            try {
                produto.setNome(edtNome.getText());
                produto.setIdProduto(edtCod.getText());
                produto.setEstoqueMin(Integer.valueOf(edtEstoqueMin.getText()));
                produto.setValor(Double.valueOf(edtValor.getText()));
                produto.setQtdEstoque(Integer.valueOf(edtQtdEstoque.getText()));
                if (pService.editar(produto)) {
                    JOptionPane.showMessageDialog(null, "Produto editado com sucesso!",
                            "Edição Produto", JOptionPane.INFORMATION_MESSAGE);
                    dispose();

                }

            } catch (NegocioException ex) {
                Logger.getLogger(CadastrarVendedor.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios!",
                    "Erro Editar Produto", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(this,
                "Cancelar a operação?", "Editar Produto",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed


